package com.rainy.framework.constant;

/**
 * 常用字符串常量
 *
 * @author Created by renguangli at 2022/9/20 23:17
 */
public interface CharConstants {

    /**
     * 字符串常量：空字符串 {@code ''}
     */
    String EMPTY = "";

    /**
     * 字符串常量：逗号 {@code ','}
     */
    String COMMA = ",";

    /**
     * 字符串常量：冒号 {@code ':'}
     */
    String COLON = ":";

    /**
     * 字符串常量：换行符 {@code '\n'}
     */
    String LF = "\n";

    /**
     * 字符串常量：横岗 {@code '-'}
     */
    String RUNG = "-";
}
